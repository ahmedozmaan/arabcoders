import turtle
import time

def draw_square():
    window = turtle.Screen()
    window.bgcolor('#cebbbe')

    brad = turtle.Turtle()
    brad.forward(100)
    brad.right(90)
    brad.forward(100)
    brad.right(90)
    brad.forward(100)
    brad.right(90)
    brad.forward(100)
    brad.right(75)



    window.exitonclick()

draw_square()
